var assert = require("assert");
describe('node_modules', function(){
    it('should be able to load the color-name module - sync', async function(){
        // update clientside_require module
        var clientside_require = require(process.env.src_root + '/index.js');
        window.clientside_require = clientside_require;
        clientside_require.modules_root = "file:///"+ process.env.test_env_root + "/node_modules";

        // retreive module
        var content = await clientside_require.asynchronous_require("color-name");
        assert.equal(typeof content, "object");
    })
    it('should be able to load the qs module - sync', async function(){
        // update clientside_require module
        var clientside_require = require(process.env.src_root + '/index.js');
        window.clientside_require = clientside_require;
        clientside_require.modules_root = "file:///"+ process.env.test_env_root + "/node_modules";

        // retreive module
        var content = await clientside_require.asynchronous_require("qs");
        assert.equal(typeof content.stringify, "function");
    })
})
